- (void)invalidateSessionCancelingTasks:(BOOL)cancelPendingTasks {
    dispatch_async(dispatch_get_main_queue(), ^{
        if (cancelPendingTasks) {
            // 取消所有的任务，包括正在执行的
            [self.session invalidateAndCancel];
        } else {
            // 会完成已经在执行的
            [self.session finishTasksAndInvalidate];
        }
    });
}

