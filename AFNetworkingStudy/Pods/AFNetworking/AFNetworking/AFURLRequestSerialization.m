- (NSMutableURLRequest *)requestWithMethod:(NSString *)method
                                 URLString:(NSString *)URLString
                                parameters:(id)parameters
                                     error:(NSError *__autoreleasing *)error
{
    NSParameterAssert(method);
    NSParameterAssert(URLString);

    NSURL *url = [NSURL URLWithString:URLString];

    NSParameterAssert(url);

    // ÊûÑÂª∫ requestÂØπË±°
    NSMutableURLRequest *mutableRequest = [[NSMutableURLRequest alloc] initWithURL:url];
    // GETÔºå POSTÁ≠âÊñπÊ≥ï
    mutableRequest.HTTPMethod = method;

    // ËÆæÁΩÆËØ∑Ê±ÇÂ§¥ÔºåÊï∞ÁªÑÈáåÂ≠òÁöÑÊòØrequestÈáåÁöÑÂ±ûÊÄßÔºåself class‰πüÊúâÁõ∏Â∫îÁöÑÂ≠óÊÆµÂØπÂ∫îrequestÔºåËÆæÁΩÆ‰∫Üself class,‰πüÂ∞±ËÆæÁΩÆ‰∫ÜrequestÔºå‰ΩÜÊòØ‰∏ìÈó®Êúâ‰∏Ä‰∏™Â≠óÂÖ∏ÊòØÊù•ÁÆ°ÁêÜheader ÁöÑmutableHTTPRequestHeadersÊù•ÁÆ°ÁêÜheaderÁöÑkey ÂíåvalueÁöÑ
    for (NSString *keyPath in AFHTTPRequestSerializerObservedKeyPaths()) {
        if ([self.mutableObservedChangedKeyPaths containsObject:keyPath]) {
            [mutableRequest setValue:[self valueForKeyPath:keyPath] forKey:keyPath];
        }
    }

    // Ê†ºÂºèÂåñÂèÇÊï∞, e.g : name=wang&password=123456Ôºå‰ΩÜÊòØ‰∏∫‰ªÄ‰πàË¶ÅmutableCopy‰∏ãÂë¢ÔºüËøôÂè•‰ª£Á†Å‰πãÂâçÔºå‰πãÂêéÔºåmutableRequest‰∏çÂ∞±ÊòØÂêå‰∏Ä‰∏™ÂØπË±°‰∫ÜÔºå‰∫ßÁîü‰∫Ü‰∏Ä‰∏™Êñ∞ÁöÑNSMutableURLRequestÂØπË±°
    mutableRequest = [[self requestBySerializingRequest:mutableRequest withParameters:parameters error:error] mutableCopy];

	return mutableRequest;
}

- (NSMutableURLRequest *)multipartFormRequestWithMethod:(NSString *)method
                                              URLString:(NSString *)URLString
                                             parameters:(NSDictionary *)parameters
                              constructingBodyWithBlock:(void (^)(id <AFMultipartFormData> formData))block
                                                  error:(NSError *__autoreleasing *)error
{
    NSParameterAssert(method);
    NSParameterAssert(![method isEqualToString:@"GET"] && ![method isEqualToString:@"HEAD"]);

    NSMutableURLRequest *mutableRequest = [self requestWithMethod:method URLString:URLString parameters:nil error:error];

    __block AFStreamingMultipartFormData *formData = [[AFStreamingMultipartFormData alloc] initWithURLRequest:mutableRequest stringEncoding:NSUTF8StringEncoding];

    // ‰∏ä‰º†Êñá‰ª∂ÁöÑË°®ÂçïÊï∞ÊçÆÔºå‰∏çÊòØÊñá‰ª∂ÁöÑ‰∫åËøõÂà∂Êï∞ÊçÆ
    if (parameters) {
        for (AFQueryStringPair *pair in AFQueryStringPairsFromDictionary(parameters)) {
            NSData *data = nil;
            if ([pair.value isKindOfClass:[NSData class]]) {
                data = pair.value;
            } else if ([pair.value isEqual:[NSNull null]]) {
                data = [NSData data];
            } else {
                data = [[pair.value description] dataUsingEncoding:self.stringEncoding];
            }

            if (data) {
                // Ê∑ªÂä†parameter Á±ª‰ºº‰∫éËøôÁßçÊ†ºÂºèÔºåContent-Disposition: form-data; name=#{name}„ÄÇ‰∏Ä‰∏™keyÔºåvalueÁöÑË°®ÂçïÊï∞ÊçÆÔºåÂè™ÊòØË∑ü‰πãÂâçÁöÑË°®ÂçïÊï∞ÊçÆÊ†ºÂºè‰∏ç‰∏ÄÊ†∑Ôºå‰∏ä‰º†Êñá‰ª∂ÊúâËá™Â∑±ÁöÑË°®ÂçïÊï∞ÊçÆÊ†ºÂºè
                [formData appendPartWithFormData:data name:[pair.field description]];
            }
        }
    }

    // Ê∑ªÂä†Êñá‰ª∂ÁöÑ‰∫åËøõÂà∂Êï∞ÊçÆ
    if (block) {
        block(formData);
    }

    // ÊûÑÂª∫‰∏ä‰º†Êñá‰ª∂ÁöÑrequestÔºå‰∏ªË¶ÅÊòØbodyÁöÑÊûÑÂª∫
    return [formData requestByFinalizingMultipartFormData];
}

- (NSMutableURLRequest *)requestWithMultipartFormRequest:(NSURLRequest *)request
                             writingStreamContentsToFile:(NSURL *)fileURL
                                       completionHandler:(void (^)(NSError *error))handler
{
    NSParameterAssert(request.HTTPBodyStream);
    NSParameterAssert([fileURL isFileURL]);

    NSInputStream *inputStream = request.HTTPBodyStream;
    NSOutputStream *outputStream = [[NSOutputStream alloc] initWithURL:fileURL append:NO];
    __block NSError *error = nil;

    dispatch_async(dispatch_get_global_queue(DISPATCH_QUEUE_PRIORITY_DEFAULT, 0), ^{
        [inputStream scheduleInRunLoop:[NSRunLoop currentRunLoop] forMode:NSDefaultRunLoopMode];
        [outputStream scheduleInRunLoop:[NSRunLoop currentRunLoop] forMode:NSDefaultRunLoopMode];

        [inputStream open];
        [outputStream open];

        while ([inputStream hasBytesAvailable] && [outputStream hasSpaceAvailable]) {
            uint8_t buffer[1024];

            NSInteger bytesRead = [inputStream read:buffer maxLength:1024];
            if (inputStream.streamError || bytesRead < 0) {
                error = inputStream.streamError;
                break;
            }

            NSInteger bytesWritten = [outputStream write:buffer maxLength:(NSUInteger)bytesRead];
            if (outputStream.streamError || bytesWritten < 0) {
                error = outputStream.streamError;
                break;
            }

            if (bytesRead == 0 && bytesWritten == 0) {
                break;
            }
        }

        [outputStream close];
        [inputStream close];

        if (handler) {
            dispatch_async(dispatch_get_main_queue(), ^{
                handler(error);
            });
        }
    });

    NSMutableURLRequest *mutableRequest = [request mutableCopy];
    mutableRequest.HTTPBodyStream = nil;

    return mutableRequest;
}

